import { execFileSync } from 'child_process';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { writeFileSync } from 'fs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const OUT_PATH = join(ROOT, 'app/_docs/registry/changelog.ts');

const TYPE_LABELS = {
	feat:     'Added',
	fix:      'Fixed',
	perf:     'Changed',
	refactor: 'Changed',
	style:    'Changed',
	chore:    'Changed',
	docs:     'Docs',
};

const CATEGORY_ORDER = ['Added', 'Fixed', 'Changed', 'Docs'];

const git = (args) => execFileSync('git', args, { cwd: ROOT, encoding: 'utf8' }).trim();

const parseCommit = (line) =>
{
	const [hash, ...rest] = line.split('\x1f');
	const subject = rest.join('\x1f');
	const match = subject.match(/^(\w+)(?:\([^)]*\))?:\s*(.+)$/);

	if(!match) return null;

	const [, type, message] = match;
	const label = TYPE_LABELS[type];

	if(!label) return null;

	return { hash, label, message: message.charAt(0).toUpperCase() + message.slice(1) };
}

const commitsInRange = (range) =>
{
	const log = git(['log', '--reverse', '--format=%H\x1f%s', range]);

	if(!log) return [];

	return log.split('\n').map(parseCommit).filter(Boolean);
}

const groupByCategory = (commits) =>
{
	const groups = {};

	for(const commit of commits)
	{
		groups[commit.label] ??= [];
		groups[commit.label].push(commit.message);
	}

	return CATEGORY_ORDER
		.filter((label) => groups[label]?.length)
		.map((label) => ({ label, changes: groups[label] }));
}

// Runs as a prebuild step on every deploy, so a git failure here (e.g. a
// shallow clone with truncated tag history) must not fail the whole build —
// fall back to whatever changelog.ts is already committed instead.
try
{
	const tags = git(['tag', '--sort=creatordate']).split('\n').filter(Boolean);

	const entries = [];
	let previousTag = null;

	for(const tag of tags)
	{
		const range = previousTag ? `${previousTag}..${tag}` : tag;
		const date = git(['log', '-1', '--format=%as', tag]);

		entries.push({
			version: tag.replace(/^v/, ''),
			date,
			sections: groupByCategory(commitsInRange(range)),
		});

		previousTag = tag;
	}

	const latestTag = tags[tags.length - 1];
	const unreleased = latestTag ? commitsInRange(`${latestTag}..HEAD`) : commitsInRange('HEAD');

	if(unreleased.length)
	{
		entries.push({
			version: 'Unreleased',
			date: null,
			sections: groupByCategory(unreleased),
		});
	}

	entries.reverse();

	const source = `// Generated by scripts/build-changelog.mjs — do not edit by hand. Run \`npm run build-changelog\`.
export interface ChangelogSection
{
	label:   string;
	changes: string[];
}

export interface ChangelogEntry
{
	version:  string;
	date:     string | null;
	sections: ChangelogSection[];
}

export const changelog: ChangelogEntry[] = ${JSON.stringify(entries, null, '\t')};
`;

	writeFileSync(OUT_PATH, source);
	execFileSync('npx', ['eslint', OUT_PATH, '--fix'], { cwd: ROOT });

	console.log(`Wrote ${entries.length} changelog entr${entries.length === 1 ? 'y' : 'ies'} to ${OUT_PATH}`);
}
catch(err)
{
	console.warn(`build-changelog: skipping regeneration — ${err.message}`);
}
